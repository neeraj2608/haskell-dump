module HUNitTesting where

import Test.QuickCheck

main :: IO()
main = quickCheck (prop_idempotent :: [Integer] -> Bool)

qsort :: Ord a => [a] -> [a]
qsort []     = []
qsort (x:xs) = qsort lhs ++ [x] ++ qsort rhs
    where lhs = filter  (< x) xs
          rhs = filter (>= x) xs

prop_idempotent xs = qsort (qsort xs) == qsort xs